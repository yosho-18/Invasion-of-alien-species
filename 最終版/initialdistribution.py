def center_cross(w, L):
    w[L // 2][L // 2] = 0.3
    w[L // 2 - 1, L // 2] = 0.3
    w[L // 2 + 1, L // 2] = 0.3
    w[L // 2, L // 2 - 1] = 0.3
    w[L // 2, L // 2 + 1] = 0.3

def cross(w, L):
    w[2][L // 2] = 0.3
    w[1, L // 2] = 0.3
    w[3, L // 2] = 0.3
    w[2, L // 2 - 1] = 0.3
    w[2, L // 2 + 1] = 0.3

def square4(w, L):
    for i in range(3):
        for j in range(3):
            w[L // 2 - i - 1][L // 2 - j - 1] = 0.3
